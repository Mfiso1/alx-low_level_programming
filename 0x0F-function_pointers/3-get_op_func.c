#include "3-calc.h"

/**
 * op_add - calculates the sum of two integers
 * @num1: first integer
 * @num2: second integer
 * Return: sum of num1 and num2
 */
int op_add(int num1, num2)
{
	return (num1 + num2);
}

/**
 * op_sub - calculates the difference of two integers
 * @num1: first integer
 * @num2: second integer
 * Return: difference of num1 and num2
 */
int op_sub(int num1, int num2)
{
	return (num1 - num2);
}

/**
 * op_mul - calculates the product of two integers
 * @num1: first integer
 * @num2: second integer
 * Return: product of num1 and num2
 */
int op_mul(int num1, int num2)
{
	return (num1 * num2);
}

/**
 * op_div - calculates the division of two integers
 * @num1: first integer
 * @num2: second integer
 * Return: result of division of num1 and num2
 */
int op_div(int num1, int num2)
{
	return (num1 / num2);
}

/**
 * op_mod - calculates the remainder of two intergers
 * @num1: first integer
 * @num2: second integer
 * Return: remainder of num1 and num2
 */
int op_mod(int num1, int num2)
{
	return (num1 % num2);
}

