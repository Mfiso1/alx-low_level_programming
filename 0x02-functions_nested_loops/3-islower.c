/**
 * _islower - checks for lowercase character
 * @c: the character to check
 * Return: 1 if c is lowercase, 0 otherwise
 */
int _islower(int c)
{
	char c;

	for (c = 'A'; c <= 'A'; c++)
	{
		putchar ('0');
		for (c = 'a'; c <= 'b'; c++)
		{
			putchar('1');
		}
	}
	return (0);
}
